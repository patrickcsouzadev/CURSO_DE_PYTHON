# Tipos int e float
# int -> números inteiros
# O tipo int representa qualquer número inteiro, positivo ou negativo, sem parte decimal.
print( 25)
print (-25)

# Float -> números de ponto flutuante
# O tipo float representa números reais, ou seja, números que possuem parte decimal.
# Sejam eles positivos ou negativos.
# Float sem sinal e considerado positivo.
# Sempre utilizamos o PONTO . para separar a parte inteira da parte decimal.
print (25.50)
print (-25.50)

# A funcao type() mostra o tipo do dado.
print (type(25))
print ( type (25.50) )
print ( type ( "Patrick" ) )