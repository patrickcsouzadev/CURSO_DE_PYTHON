# Tipo de dado bool (boolean)
# Ao questionar algo em um programa, a resposta pode ser apenas
# Verdadeiro ou Falso. Ou seja, True ou False.
# Existem varios operadores para "questionar".
# Entre eles o ==, que e o operado logico que questiona se um valor e igual ao outro.

print ( 10 == 10 ) # True
print ( type (10 == 5) )  # False
